    // 1. 恋选择路径最大化路径上的w[i][j]之和
    // 2. 觉在重叠区域放下她路径上的高价值物品
    
    // 但实际上，觉放下物品后，恋看到的是该格子当前所有物品
    // 所以恋的收益 = 路径上所有格子的w[i][j]之和 + 觉在重叠区域放下的物品价值
    
    // 贪心策略：
    // 1. 恋选择从(0,0)到(xk,yk)的最优路径（最大化w[i][j]之和）
    // 2. 觉在重叠区域放下她路径上不在恋路径上的高价值物品
    
    // 计算恋的最优路径
    vector<vector<long long>> dp恋(n, vector<long long>(m, -1e18));
    dp恋[0][0] = w[0][0];
    for (int i = 0; i <= xk; i++) {
        for (int j = 0; j <= yk; j++) {
            if (i == 0 && j == 0) continue;
            if (i > 0) dp恋[i][j] = max(dp恋[i][j], dp恋[i-1][j] + w[i][j]);
            if (j > 0) dp恋[i][j] = max(dp恋[i][j], dp恋[i][j-1] + w[i][j]);
        }
    }
    
    long long base = dp恋[xk][yk];
    
