    // 恋的路径：从(0,0)到(xk,yk)，只能向右或向下
    // 可达格子：(i,j) where i<=xk, j<=yk
    
    // 觉的路径：从(0,0)到(xs,ys)，只能向右或向下
    // 可达格子：(i,j) where i<=xs, j<=ys
    
    // 重叠区域：i<=min(xs,xk), j<=min(ys,yk)
    
    // 在重叠区域，觉可以将高价值物品放下，恋可以捡起
    // 非重叠区域，恋只能看到原始物品
    
