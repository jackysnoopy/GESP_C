    // 恋在该格子看到的价值 = 原始w[i][j] + 放下的物品价值
    
    // 实际上，觉可以将她路径上的物品转移到恋路径上的任何格子
    // 只要该格子在恋的路径上
    
    // 计算觉在重叠区域可以额外贡献的价值
    // 觉的路径上的物品价值之和 - 恋路径上已经包含的物品价值
    
    // 这是一个复杂的优化问题，暂时用简单方法
    
    // 简单方法：恋选择最优路径，觉在重叠区域放下高价值物品
