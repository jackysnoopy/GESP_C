/*
 * GCJ 2015 1B - Counter Culture
 * 
 * 从1开始，每次可以 +1 或者反转数字（去掉前导0），
 * 求到达N的最小步数。
 * 
 * 正确解法（贪心分段）：
 * 
 * 核心思想：对于 d 位数 N，将 N 拆成高位 a 和低位 b：
 *   N = a * 10^l + b，其中 l = d/2, a 有 d-l 位
 * 
 * 最优策略：先到达 a*10^l，然后数 b 步到 N。
 * 到 a*10^l 的步数：
 *   如果 a*10^l 以0结尾 -> 先到 a*10^l - 1，再+1
 *   到 a*10^l - 1：后l位全是9
 *     = 到 (a-1)*10^l 的步数 + (10^l - 1)
 *     = 到 (a-1)*10^l - 1 的步数 + 1 + (10^l - 1)
 *   
 * 但这样递归太慢。正确做法：
 * 
 * 递归公式：
 *   f(1) = 1
 *   f(N): d = digits(N), l = floor(d/2), h = d-l
 *         low = 10^l
 *         a = N/low, b = N%low
 *         
 *         如果 b == 0: f(N) = f(N-1) + 1
 *         否则: f(N) = f(a * low) + b
 *         
 *   其中 f(a*low) 需要处理以0结尾的情况。
 *   但 f(a*low) = f(a*low - 1) + 1
 *   而 a*low - 1 = (a-1)*low + (low-1)
 *   f((a-1)*low + low-1) = f((a-1)*low) + (low-1)
 *   
 *   所以 f(a*low) = f((a-1)*low) + low - 1 + 1 = f((a-1)*low) + low
 *   递推: f(a*low) = f(low) + (a-1)*low
 *   f(low) = f(low-1) + 1
 *   
 *   low-1 是一个 l 位全9的数。
 *   对于 l 位全9的数，f(10^l - 1) = 10^l - 1（直接数过去，翻转无帮助）
 *   
 *   所以 f(low) = 10^l - 1 + 1 = 10^l = low
 *   f(a*low) = low + (a-1)*low = a*low = N（直接数到N...）
 *   
 *   这不对！翻转应该能节省步数。
 *   
 * 关键错误：f(10^l - 1) 不一定等于 10^l - 1！
 * 当 l >= 2 时，可以通过翻转加速到达 10^l - 1。
 * 
 * 例如到99: 1->...->12(12步)->rev(12)=21(13步)->...->99(99步)
 * 虽然最后还是99步，但到999: 
 *   到12=12步, rev=21(13), 到99=99步, 到100=100步
 *   到1001: 到12=12, rev=21, 到99=99, 到100=100, 到1001=1001
 *   但也可以: 到12=12, rev=21, 到99=99, +1到100, +1到101, ...到1001(1001步)
 *   
 * 翻转在大数时的真正用处：
 * 到 10^k: 10^k 步（无法翻转加速）
 * 到 10^k + 1: 可以先到10^k, 然后rev(10^k)不行(前导0)
 *   所以也是 10^k + 1 步
 * 
 * 到 1001:
 *   1->...->99(99) -> 100(100) -> 1001(1001步)
 *   或 1->...->12(12) -> rev=21(13) -> ... -> 99(99) -> 100(100) -> ... -> 1001
 *   
 * 翻转真正有用的例子：
 * 到 19: 1->...->19 = 19步
 * 到 23: 1->...->12(12) -> rev=21(13) -> 22(14) -> 23(15步) ✅ 比直接数到23(23步)少！
 * 
 * 到 100:
 *   1->...->100 = 100步（翻转无用，因为100反转=1）
 * 
 * 所以翻转只在"到达某个中间值后反转可以跳到更大的值"时有用
 * 这个中间值的特点是：反转后 > 中间值
 * 即：反转后首位 > 中间值的末位
 * 
 * 对于 d 位数 N = a*10^l + b：
 *   最优策略是：先数到某个值 x，反转到 rev(x)，再数到 N
 *   x 应该满足: rev(x) 尽可能大（接近 N），且 x 尽可能小（减少到达x的步数）
 *   
 *   x 有 d 位，x = c*10^l + d_val
 *   rev(x) = rev(d_val)*10^(d-l) + rev(c) 如果 d_val 有 l 位
 *   
 * 实际上 GCJ 标准解法是：
 * 
 * f(N): 从1到N的最小步数
 * 
 * 对于 d 位数 N (d >= 2)：
 *   h = ceil(d/2), l = d - h
 *   low = 10^l
 *   a = N / low  (h位)
 *   b = N % low  (l位)
 *   
 *   最优策略：到达 a*low 后数 b 步
 *   到 a*low 的最优步数：
 *     = f(a) * (某种方式翻转到 a*low)
 *     实际上：到 a*low = 先到 a，翻转 a 到 rev(a)，然后 rev(a) 有 h 位或更少
 *     如果 rev(a) < a*low: 从 rev(a) 数到 a*low: a*low - rev(a) 步
 *     总步数: f(a) + 1 + (a*low - rev(a))
 *     
 *     但 a 可能需要先到某个值再翻转到 a：
 *     f(a) 本身也是递归的
 *     
 *     更精确：
 *     到 a*low = 先数到 a (f(a)步), +1次翻转, 然后从 rev(a) 数到 a*low
 *     = f(a) + 1 + (a*low - rev(a))
 *     这是对的！因为：
 *       我们可以先按最优方式数到 a，此时说了 a 这个数
 *       然后翻转 a 得到 rev(a)（说了 rev(a)）
 *       然后从 rev(a) 数到 a*low
 *     
 *     但等等：如果 a 有前导0怎么办？a 是 h 位数，不会有前导0
 *     rev(a) 是 a 反转，可能比 a 小很多
 *     
 *     例如 N=23, d=2, h=1, l=1, low=10, a=2, b=3
 *     f(2*10) = f(2) + 1 + (20 - rev(2)) = 2 + 1 + (20 - 2) = 21
 *     f(23) = f(20) + 3 = 21 + 3 = 24 ❌ 应该是15
 *     
 *     问题在于：我们不需要先到 a=2 再翻转到20
 *     而是：到 12 (12步) -> rev(12)=21(13步) -> 22(14步) -> 23(15步)
 *     到 a*low=20 的步数应该更小
 *     
 * 所以正确的策略不是翻转 a，而是翻转一个"半满"的数
 * 正确的GCJ解法（最终版）：
 * 
 * 对于 d 位数 N：
 *   h = ceil(d/2), l = floor(d/2)
 *   low = 10^l
 *   
 *   if N < 10^h:  // d位数但高位是1
 *     f(N) = f(10^h) + (N - 10^h)  // 但 f(10^h) 怎么算？
 *     或者 f(N) = N (直接数)
 *     
 *   实际上GCJ编辑器的解法是：
 *   到 10^h - 1 的步数: g(h) = 到 10^h - 1 的最优步数
 *   到 N 的步数: g(h) + (N - (10^h - 1)) 如果不翻转
 *   或者: g(h) + 1 + (N - rev(10^h + a - 10^l)) 翻转
 *   
 * 这太复杂了，让我直接用BFS/DP验证小数据，然后找规律
 */

// 对小N用BFS验证
ll bfsSmall(ll N) {
    // BFS从1出发，每次+1或反转
    // N最多到10000
    if (N <= 1) return N;
    int limit = (int)N + 100;
    vector<ll> dist(limit + 1, -1);
    dist[1] = 1; // 从1开始，说了1这个数
    
    // BFS
    // 实际上是从1开始，每次操作后说一个新数
    // 起始: 已经说了1，dist[1] = 1
    // +1操作: dist[x+1] = dist[x] + 1
    // 翻转操作: dist[rev(x)] = dist[x] + 1 (如果 rev(x) 未访问或更优)
    
    // 因为是BFS，可以用队列
    // 但数字可达N，N最大10000，可以BFS
    
    // 用Dijkstra（所有边权为1，BFS即可）
    // 但要小心：翻转可能跳到比当前小的数
    
    for (int i = 1; i <= limit; i++) dist[i] = 0;
    dist[1] = 1;
    
    // 用数组模拟BFS
    // 因为翻转可能跳到更小的数，需要用优先队列或多次迭代
    // 用简单的多次松弛
    bool changed = true;
    while (changed) {
        changed = false;
        for (int i = 1; i <= min((int)N, limit); i++) {
            if (dist[i] == 0) continue;
            // +1
            if (i + 1 <= limit && (dist[i + 1] == 0 || dist[i] + 1 < dist[i + 1])) {
                dist[i + 1] = dist[i] + 1;
                changed = true;
            }
            // rev
            ll r = 0, tmp = i;
            while (tmp > 0) { r = r * 10 + tmp % 10; tmp /= 10; }
            if (r <= limit && r != i && (dist[r] == 0 || dist[i] + 1 < dist[r])) {
                dist[r] = dist[i] + 1;
                changed = true;
            }
        }
    }
    return dist[N];
}

// 验证规律
void verify() {
    cout << "BFS results for small N:" << endl;
    for (ll n = 1; n <= 100; n++) {
        cout << n << ": " << bfsSmall(n) << endl;
    }
}

