sol = {"P16354": """### 问题分析
- 南北向道路规划 $n$ 栋楼，楼高为 $1\\sim n$ 的排列。从北向南能看见=高于北侧所有楼；从南向北能看见=高于南侧所有楼。给 01 串 $a,b,c$：$a_i=1$ 须北向可见、$b_i=1$ 须南向可见、$c_i=1$ 须两侧均不可见。求满足的规划方案（通常字典序最小）。
- 经典"可见楼"排列问题，叠加三组位置约束。

### 核心思路
- 楼高排列中，从一端可见的楼构成递增的"前缀最大值"序列。
- $a,b$ 约束指定某些位置必须是某侧的前缀最大值；$c$ 约束指定某些位置两侧都不是前缀最大值。
- 贪心构造：按位置分配高度，使指定的可见性约束满足；通常用栈/笛卡尔树结构，从大到小分配高度，保证约束位置成为峰值，并取字典序最小。

### 算法流程
1. 解析 $a,b,c$ 约束。
2. 按可见性约束贪心分配高度（峰值位置给高、隐蔽位置给低）。
3. 输出满足约束的排列（字典序最小）或判无解。

### 复杂度分析
- 时间复杂度：$O(n)$。
- 空间复杂度：$O(n)$。"""}
